        $filterKeyword = $request->get('name');
 
        if($filterKeyword){
 
            $transaksi = \App\Transaksi::where("name", "LIKE", "%$filterKeyword%")->paginate(10);
        }

        return view('transaksis.index', ['transaksis' => $transaksi]);
